/*
-Polymorphism in JavaScript, especially in Object-Oriented Programming (OOP), refers to the ability of a single function or method to behave differently based on the object it is called on or the context in which it is used. It is one of the key principles of OOP, alongside inheritance, encapsulation, and abstraction.

In JavaScript, polymorphism can be implemented in several ways:
    --1. Method Overriding
    --2. Interface-Like Behavior
    --3. Function Overloading (Simulated)

Benefits of Polymorphism
    -Flexibility: Code can handle different types of objects in a uniform way.
    -Reusability: Reduces duplication by reusing the same interface.
    -Extensibility: Makes it easier to add new functionalities without altering existing code.
    -Polymorphism is a key concept to ensure that your code remains modular, -maintainable, and extensible in JavaScript's OOP paradigm.

*/